

class Calculadora {
  
  int? valor1;
  int? valor2;
  String? operador;

  num soma(num valor1, num valor2) {
    //double adicao = valor1 + valor2;
    return valor1 + valor2;
  }

  num minus(num valor1, num valor2) {
    //double subtracao = valor1 - valor2;
    return valor1 - valor2;
  }

  num multiply(num valor1, num valor2) {
    //double multiplicacao = valor1 * valor2;
    return valor1 * valor2;
  }

  num divided(num valor1, num valor2) {
    //double divisao = valor1 / valor2;
    return valor1 / valor2;
  }
}
